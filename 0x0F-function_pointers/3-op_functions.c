#include "3-calc.h"

/**
 * op_add - add two number
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subract two number
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mltiply two integer
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two integer
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculate reminder
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
